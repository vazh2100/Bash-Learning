#!/bin/bash

is_ip_valid() {
  case $1 in
  "" | *[!0-9.]* | *[!0-9]) return 1 ;;
  esac
  local IFS=. # Устанавливаем разделитель
  set -- $1   # Добавляем в позиционные параметры слова разделённые разделителем
  [ $# -eq 4 ] && [ "${1:-999}" -le 255 ] && [ "${2:-999}" -le 255 ] && [ "${3:-999}" -le 255 ] && [ "${4:-999}" -le 255 ]
}
